namespace markdown {
    public static class CharCode {
        public const int exclamation = 0x21; // ! 
        public const int quote = 0x22; // " 
        public const int hash = 0x23; // # 
        public const int dollar = 0x24; // $ 
        public const int percent = 0x25; // % 
        public const int ampersand = 0x26; // & 
        public const int apostrophe = 0x27; // '.
        public const int lparen = 0x28; // ( 
        public const int rparen = 0x29; // ) 
        public const int asterisk = 0x2A; // * 
        public const int plus = 0x2B; // + 
        public const int comma = 0x2C; // , 
        public const int dash = 0x2D; // - 
        public const int dot = 0x2E; // . 
        public const int slash = 0x2F; // \ 
        public const int colon = 0x3A; // ; 
        public const int semicolon = 0x3B; // ; 
        public const int lt = 0x3C; // < 
        public const int equal = 0x3D; // = 
        public const int gt = 0x3E; // > 
        public const int question = 0x3F; // ? 
        public const int at = 0x40; // @ 
        public const int lbracket = 0x5B; // [ 
        public const int backslash = 0x5C; // \ 
        public const int rbracket = 0x5D; // ] 
        public const int caret = 0x5E; // ^ 
        public const int underscore = 0x5F; // _ 
        public const int backquote = 0x60; // ` 
        public const int lbrace = 0x7B; // { 
        public const int bar = 0x7C; // | 
        public const int rbrace = 0x7D; // } 
        public const int tilde = 0x7E; // ~ 
        public const int space = 0x20; // Space character. 
        public const int tab = 0x09; // "Horizontal Tab" control character, common name.
        public const int lf = 0x0A; // "Line feed" control character. 
        public const int vt = 0x0B; // "Vertical Tab" control character. 
        public const int cr = 0x0D; // "Carriage return" control character.
        public const int ff = 0x0C; // "Form feed" control character.
    }
}